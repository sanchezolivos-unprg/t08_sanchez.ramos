# ITERACION 1:
mensaje="ASI ES LA VIDA"
contador_a=0
contador_e=0
contador_i=0
contador_o=0
contador_u=0

for letra in mensaje:
    if (letra == 'A'):
        contador_a+=1
    if (letra == 'E'):
        contador_e+=1
    if (letra == 'I'):
        contador_i+=1
    if (letra == 'O'):
        contador_o+=1
    if (letra == 'U'):
        contador_u+=1

# Contar cuantas vocales existen en la cadena
print("A: ",contador_a)
print("E: ",contador_e)
print("I: ",contador_i)
print("O: ",contador_o)
print("U: ",contador_u)
# FIN_ITERACION
print()

